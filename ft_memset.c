#include "libft.h"

void	*ft_memset(void *str, int c, size_t len)
{
	char	*s;
	size_t	i;

	s = (char*)str;
	i = 0;
	while (i < len)
	{
		s[i] = c;
		i++;
	}
	return (str);
}
